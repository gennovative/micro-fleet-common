export enum WebSettingKeys {

    /**
     * Configuration for Cross-Origin Resource Sharing.
     * Type: string | string[]
     */
    WEB_CORS = 'web_cors',

    /**
     * HTTP port listened by webserver.
     * Type: number
     */
    WEB_PORT = 'web_port',

    /**
     * Prefix to route url.
     * Type: string
     */
    WEB_URL_PREFIX = 'web_url_prefix',
}
